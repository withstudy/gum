#!/usr/bin/env node
const yargs = require('yargs/yargs')
const { hideBin } = require('yargs/helpers')
const list = require('../lib/list.js')
const add = require('../lib/add.js')
const del = require('../lib/del.js')
const use = require('../lib/use.js')
yargs(hideBin(process.argv))
    .command(list)
    .command(add)
    .command(del)
    .command(use)
    .strictCommands()
    .demandCommand(1)
    .alias('h', 'help')
    .alias('v', 'version')
    .parse()